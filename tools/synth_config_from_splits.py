#!/usr/bin/env python3
"""
Patch or synthesize build/GL5E4F/config.json from splits.txt.

This is a project-side workaround for dtk split panics. It preserves the
section-level fallback units needed by the current build flow, but also merges
in split-defined object units so configure.py / objdiff.json can see recovered
source files.
"""

import argparse
import json
import re
import subprocess
import sys
from pathlib import Path
from typing import Any

SRC_EXTS = (".c", ".cp", ".cpp")
MAKE_TARGET = Path(__file__).resolve().parent / "make_target_obj.py"


def regen_target_objects(file_entries: list[dict[str, Any]], src_dir: Path) -> None:
    """Build an objdiff target object for every decompiled source unit.

    Without this, objdiff/progress silently skips a unit until someone remembers
    to create build/<ver>/obj/<unit>.o by hand. Running it here (the SPLIT step)
    keeps matching progress in sync with the source on every configure.
    """
    for entry in file_entries:
        name = entry["name"]
        if not name.endswith(SRC_EXTS):
            continue
        if not (src_dir / name).exists():
            continue
        result = subprocess.run(
            [sys.executable, str(MAKE_TARGET), name],
            capture_output=True, text=True,
        )
        if result.returncode != 0:
            # Non-fatal: a unit may lack a clean asm slice yet. Keep configuring.
            sys.stderr.write(f"make_target_obj {name}: {result.stderr.strip()}\n")


